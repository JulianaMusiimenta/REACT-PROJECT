//1 What is JSX and why is it used?
// JSX is a syntax extension for JavaScript that allows developers to write 
// HTML-like code within JavaScript files. 
// It is used in React to describe what the UI should look like. 
// JSX makes it easier to visualize the structure of the UI 
// and allows for a more declarative approach to building components.
//  When JSX is compiled, it transforms into regular JavaScript function calls, 
// which can then be rendered by React.

// 2 Difference between props and state?
// props are used to pass data from a parent component to a child component.
// They are immutable and cannot be changed by the child component.

// State refers to the way fo manage data within a component that can change over time.
// State is mutable and can be updated using the setState function.

// 3 What are hooks in React?
// Hooks are functions that allow developers to use state and other React features
// in functional components. 





